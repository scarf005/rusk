use rusk::{SourceMapNode, SourceSyntax, to_rust, transpile};

const GENERATED_HEADER: &str = "// @generated by rusk; do not edit.\n";

#[derive(Debug)]
struct Backend {
    client: Client,
    documents: Arc<RwLock<HashMap<Url, String>>>,
    rust_analyzer: Arc<RwLock<Option<Arc<RustAnalyzerClient>>>>,
    initialize_params: Arc<RwLock<Option<InitializeParams>>>,
}

#[tower_lsp::async_trait]
impl LanguageServer for Backend {
    async fn initialize(&self, params: InitializeParams) -> Result<InitializeResult> {
        self.initialize_params.write().await.replace(params);

        Ok(InitializeResult {
            capabilities: ServerCapabilities {
                text_document_sync: Some(TextDocumentSyncCapability::Kind(
                    TextDocumentSyncKind::FULL,
                )),
                document_symbol_provider: Some(OneOf::Left(true)),
                hover_provider: Some(tower_lsp::lsp_types::HoverProviderCapability::Simple(true)),
                definition_provider: Some(OneOf::Left(true)),
                completion_provider: Some(CompletionOptions {
                    resolve_provider: Some(false),
                    trigger_characters: None,
                    all_commit_characters: None,
                    work_done_progress_options: Default::default(),
                    completion_item: None,
                }),
                ..ServerCapabilities::default()
            },
            server_info: None,
        })
    }

    async fn initialized(&self, params: InitializedParams) {
        if let Some(rust_analyzer) = self.rust_analyzer.read().await.as_ref() {
            rust_analyzer.notify("initialized", params).await;
        }
        self.client
            .log_message(MessageType::INFO, "rusk language server initialized")
            .await;
    }

    async fn shutdown(&self) -> Result<()> {
        if let Some(rust_analyzer) = self.rust_analyzer.read().await.as_ref() {
            let _ = rust_analyzer.request("shutdown", ()).await;
        }
        Ok(())
    }

    async fn did_open(&self, params: DidOpenTextDocumentParams) {
        let uri = params.text_document.uri;
        let version = params.text_document.version;
        let text = params.text_document.text;
        self.documents
            .write()
            .await
            .insert(uri.clone(), text.clone());
        self.open_rust_document(&uri, &text, version).await;
        self.publish_diagnostics(uri, text).await;
    }

    async fn did_change(&self, params: DidChangeTextDocumentParams) {
        let uri = params.text_document.uri;
        let version = params.text_document.version;
        let text = params
            .content_changes
            .into_iter()
            .last()
            .map(|change| change.text)
            .unwrap_or_default();
        self.documents
            .write()
            .await
            .insert(uri.clone(), text.clone());
        self.change_rust_document(&uri, &text, version).await;
        self.publish_diagnostics(uri, text).await;
    }

    async fn did_close(&self, params: DidCloseTextDocumentParams) {
        self.documents
            .write()
            .await
            .remove(&params.text_document.uri);
        self.close_rust_document(&params.text_document.uri).await;
        self.client
            .publish_diagnostics(params.text_document.uri, Vec::new(), None)
            .await;
    }

    async fn document_symbol(
        &self,
        params: DocumentSymbolParams,
    ) -> Result<Option<DocumentSymbolResponse>> {
        let documents = self.documents.read().await;
        let Some(text) = documents.get(&params.text_document.uri) else {
            return Ok(None);
        };
        if source_syntax(&params.text_document.uri) != SourceSyntax::Rusk {
            return Ok(Some(DocumentSymbolResponse::Nested(Vec::new())));
        }
        let Ok(output) = transpile(text) else {
            return Ok(None);
        };

        Ok(Some(DocumentSymbolResponse::Nested(
            output
                .source_tree
                .iter()
                .filter_map(symbol_from_node)
                .collect(),
        )))
    }

    async fn hover(&self, params: HoverParams) -> Result<Option<Hover>> {
        let uri = params
            .text_document_position_params
            .text_document
            .uri
            .clone();
        let position = params.text_document_position_params.position;
        let documents = self.documents.read().await;
        let Some(text) = documents.get(&uri) else {
            return Ok(None);
        };

        if let Some(rust_analyzer) = self.rust_analyzer.read().await.as_ref()
            && let Some(mut hover) = rust_analyzer.hover(text, params).await
        {
            let syntax = source_syntax(&uri);
            hover.range = hover
                .range
                .map(|range| rust_range_to_source_range(text, syntax, range));
            return Ok(Some(hover));
        }

        Ok(hover_at_position(text, position))
    }

    async fn goto_definition(
        &self,
        params: GotoDefinitionParams,
    ) -> Result<Option<GotoDefinitionResponse>> {
        let uri = params
            .text_document_position_params
            .text_document
            .uri
            .clone();
        let position = params.text_document_position_params.position;
        let documents = self.documents.read().await;
        let Some(text) = documents.get(&uri) else {
            return Ok(None);
        };

        if let Some(rust_analyzer) = self.rust_analyzer.read().await.as_ref()
            && let Some(definition) = rust_analyzer.goto_definition(text, params).await
            && let Some(rust_uri) = rust_analyzer.rust_uri_for_rusk_uri(&uri)
        {
            return Ok(Some(rust_definition_to_rusk_definition(
                text,
                source_syntax(&uri),
                &uri,
                &rust_uri,
                definition,
            )));
        }

        let Some(range) = definition_range_at_position(text, position) else {
            return Ok(None);
        };

        Ok(Some(GotoDefinitionResponse::Scalar(Location {
            uri,
            range,
        })))
    }

    async fn completion(&self, params: CompletionParams) -> Result<Option<CompletionResponse>> {
        let documents = self.documents.read().await;
        let Some(text) = documents.get(&params.text_document_position.text_document.uri) else {
            return Ok(None);
        };

        if let Some(rust_analyzer) = self.rust_analyzer.read().await.as_ref()
            && let Some(completion) = rust_analyzer.completion(text, params).await
        {
            return Ok(Some(completion));
        }

        Ok(Some(CompletionResponse::Array(completion_items(text))))
    }
}

impl Backend {
    async fn ensure_rust_analyzer(&self) -> Option<Arc<RustAnalyzerClient>> {
        if let Some(rust_analyzer) = self.rust_analyzer.read().await.as_ref().cloned() {
            return Some(rust_analyzer);
        }

        let params = self.initialize_params.read().await.as_ref().cloned()?;
        let source_root_uri = source_root_uri(&params)?;
        let rust_root_uri = target_rusk_root_uri(&source_root_uri)?;
        match RustAnalyzerClient::spawn(
            rust_initialize_params(params, &rust_root_uri),
            source_root_uri,
            rust_root_uri,
        )
        .await
        {
            Ok(rust_analyzer) => {
                let rust_analyzer = Arc::new(rust_analyzer);
                rust_analyzer
                    .notify("initialized", InitializedParams {})
                    .await;
                self.rust_analyzer
                    .write()
                    .await
                    .replace(rust_analyzer.clone());
                Some(rust_analyzer)
            }
            Err(error) => {
                self.client
                    .log_message(
                        MessageType::WARNING,
                        format!("rust-analyzer proxy disabled: {error}"),
                    )
                    .await;
                None
            }
        }
    }

    async fn open_rust_document(&self, uri: &Url, text: &str, version: i32) {
        let Some(rust_uri) = self.rust_uri_for_rusk_uri(uri).await else {
            return;
        };
        let Ok(rust) = to_rust(text, source_syntax(uri)) else {
            return;
        };
        self.copy_generated_cargo_manifests(uri).await;
        write_generated_rust_file(&rust_uri, &rust);
        let Some(rust_analyzer) = self.ensure_rust_analyzer().await else {
            return;
        };

        rust_analyzer
            .notify(
                "textDocument/didOpen",
                DidOpenTextDocumentParams {
                    text_document: TextDocumentItem {
                        uri: rust_uri,
                        language_id: "rust".to_string(),
                        version,
                        text: rust,
                    },
                },
            )
            .await;
    }

    async fn change_rust_document(&self, uri: &Url, text: &str, version: i32) {
        let Some(rust_uri) = self.rust_uri_for_rusk_uri(uri).await else {
            return;
        };
        let Ok(rust) = to_rust(text, source_syntax(uri)) else {
            return;
        };
        self.copy_generated_cargo_manifests(uri).await;
        write_generated_rust_file(&rust_uri, &rust);
        let Some(rust_analyzer) = self.ensure_rust_analyzer().await else {
            return;
        };

        rust_analyzer
            .notify(
                "textDocument/didChange",
                DidChangeTextDocumentParams {
                    text_document: VersionedTextDocumentIdentifier {
                        uri: rust_uri,
                        version,
                    },
                    content_changes: vec![TextDocumentContentChangeEvent {
                        range: None,
                        range_length: None,
                        text: rust,
                    }],
                },
            )
            .await;
    }

    async fn close_rust_document(&self, uri: &Url) {
        let Some(rust_analyzer) = self.rust_analyzer.read().await.as_ref().cloned() else {
            return;
        };
        let Some(rust_uri) = self.rust_uri_for_rusk_uri(uri).await else {
            return;
        };

        rust_analyzer
            .notify(
                "textDocument/didClose",
                DidCloseTextDocumentParams {
                    text_document: tower_lsp::lsp_types::TextDocumentIdentifier { uri: rust_uri },
                },
            )
            .await;
    }

    async fn rust_uri_for_rusk_uri(&self, uri: &Url) -> Option<Url> {
        let params = self.initialize_params.read().await;
        let source_root_uri = source_root_uri(params.as_ref()?)?;
        let rust_root_uri = target_rusk_root_uri(&source_root_uri)?;
        rust_uri_for_rusk_uri(uri, &source_root_uri, &rust_root_uri)
    }

    async fn copy_generated_cargo_manifests(&self, uri: &Url) {
        let params = self.initialize_params.read().await;
        let Some(source_root_uri) = params.as_ref().and_then(source_root_uri) else {
            return;
        };
        let Some(rust_root_uri) = target_rusk_root_uri(&source_root_uri) else {
            return;
        };
        copy_generated_cargo_manifests(uri, &source_root_uri, &rust_root_uri);
    }

    async fn publish_diagnostics(&self, uri: Url, text: String) {
        let diagnostics = match to_rust(&text, source_syntax(&uri)) {
            Ok(_) => Vec::new(),
            Err(error) => vec![Diagnostic {
                range: line_range(&text, error.line),
                severity: Some(DiagnosticSeverity::ERROR),
                source: Some("rusk".to_string()),
                message: error.to_string(),
                ..Diagnostic::default()
            }],
        };
        self.client
            .publish_diagnostics(uri, diagnostics, None)
            .await;
    }
}

#[derive(Debug)]
struct RustAnalyzerClient {
    sender: mpsc::UnboundedSender<serde_json::Value>,
    pending: Arc<Mutex<HashMap<u64, oneshot::Sender<serde_json::Value>>>>,
    next_id: AtomicU64,
    source_root_uri: Url,
    rust_root_uri: Url,
    _child: Arc<Mutex<tokio::process::Child>>,
}

impl RustAnalyzerClient {
    async fn spawn(
        params: InitializeParams,
        source_root_uri: Url,
        rust_root_uri: Url,
    ) -> std::io::Result<Self> {
        let mut child = Command::new("rust-analyzer")
            .stdin(Stdio::piped())
            .stdout(Stdio::piped())
            .stderr(Stdio::null())
            .spawn()?;
        let stdin = child.stdin.take().ok_or_else(|| {
            std::io::Error::new(
                std::io::ErrorKind::BrokenPipe,
                "rust-analyzer stdin unavailable",
            )
        })?;
        let stdout = child.stdout.take().ok_or_else(|| {
            std::io::Error::new(
                std::io::ErrorKind::BrokenPipe,
                "rust-analyzer stdout unavailable",
            )
        })?;
        let (sender, mut receiver) = mpsc::unbounded_channel::<serde_json::Value>();
        let pending = Arc::new(Mutex::new(HashMap::new()));
        let reader_pending = pending.clone();

        tokio::spawn(async move {
            let mut stdin = stdin;
            while let Some(message) = receiver.recv().await {
                if write_lsp_message(&mut stdin, &message).await.is_err() {
                    break;
                }
            }
        });
        tokio::spawn(async move {
            read_rust_analyzer_messages(stdout, reader_pending).await;
        });

        let client = Self {
            sender,
            pending,
            next_id: AtomicU64::new(1),
            source_root_uri,
            rust_root_uri,
            _child: Arc::new(Mutex::new(child)),
        };
        client.request("initialize", params).await.ok_or_else(|| {
            std::io::Error::new(
                std::io::ErrorKind::TimedOut,
                "rust-analyzer did not initialize",
            )
        })?;
        Ok(client)
    }

    async fn hover(&self, text: &str, mut params: HoverParams) -> Option<Hover> {
        let source_uri = params
            .text_document_position_params
            .text_document
            .uri
            .clone();
        params.text_document_position_params.text_document.uri =
            self.rust_uri_for_rusk_uri(&source_uri)?;
        params.text_document_position_params.position = source_position_to_rust_position(
            text,
            source_syntax(&source_uri),
            params.text_document_position_params.position,
        )?;
        self.request("textDocument/hover", params)
            .await
            .and_then(|value| serde_json::from_value(value).ok())
    }

    async fn goto_definition(
        &self,
        text: &str,
        mut params: GotoDefinitionParams,
    ) -> Option<GotoDefinitionResponse> {
        let source_uri = params
            .text_document_position_params
            .text_document
            .uri
            .clone();
        params.text_document_position_params.text_document.uri =
            self.rust_uri_for_rusk_uri(&source_uri)?;
        params.text_document_position_params.position = source_position_to_rust_position(
            text,
            source_syntax(&source_uri),
            params.text_document_position_params.position,
        )?;
        self.request("textDocument/definition", params)
            .await
            .and_then(|value| serde_json::from_value(value).ok())
    }

    async fn completion(
        &self,
        text: &str,
        mut params: CompletionParams,
    ) -> Option<CompletionResponse> {
        let source_uri = params.text_document_position.text_document.uri.clone();
        params.text_document_position.text_document.uri =
            self.rust_uri_for_rusk_uri(&source_uri)?;
        params.text_document_position.position = source_position_to_rust_position(
            text,
            source_syntax(&source_uri),
            params.text_document_position.position,
        )?;
        self.request("textDocument/completion", params)
            .await
            .and_then(|value| serde_json::from_value(value).ok())
    }

    fn rust_uri_for_rusk_uri(&self, uri: &Url) -> Option<Url> {
        rust_uri_for_rusk_uri(uri, &self.source_root_uri, &self.rust_root_uri)
    }

    async fn notify<T: serde::Serialize>(&self, method: &str, params: T) {
        let Ok(params) = serde_json::to_value(params) else {
            return;
        };
        let _ = self.sender.send(serde_json::json!({
            "jsonrpc": "2.0",
            "method": method,
            "params": params,
        }));
    }

    async fn request<T: serde::Serialize>(
        &self,
        method: &str,
        params: T,
    ) -> Option<serde_json::Value> {
        let id = self.next_id.fetch_add(1, Ordering::Relaxed);
        let params = serde_json::to_value(params).ok()?;
        let (sender, receiver) = oneshot::channel();
        self.pending.lock().await.insert(id, sender);
        if self
            .sender
            .send(serde_json::json!({
                "jsonrpc": "2.0",
                "id": id,
                "method": method,
                "params": params,
            }))
            .is_err()
        {
            self.pending.lock().await.remove(&id);
            return None;
        }
        tokio::time::timeout(std::time::Duration::from_secs(5), receiver)
            .await
            .ok()
            .and_then(std::result::Result::ok)
    }
}

async fn read_rust_analyzer_messages(
    stdout: tokio::process::ChildStdout,
    pending: Arc<Mutex<HashMap<u64, oneshot::Sender<serde_json::Value>>>>,
) {
    let mut reader = BufReader::new(stdout);
    loop {
        let Some(message) = read_lsp_message(&mut reader).await else {
            break;
        };
        let Some(id) = message.get("id").and_then(serde_json::Value::as_u64) else {
            continue;
        };
        let response = message
            .get("result")
            .cloned()
            .unwrap_or(serde_json::Value::Null);
        if let Some(sender) = pending.lock().await.remove(&id) {
            let _ = sender.send(response);
        }
    }
}

async fn read_lsp_message<R: tokio::io::AsyncBufRead + Unpin>(
    reader: &mut R,
) -> Option<serde_json::Value> {
    let mut content_length = None;
    loop {
        let mut line = String::new();
        if reader.read_line(&mut line).await.ok()? == 0 {
            return None;
        }
        let line = line.trim_end_matches(['\r', '\n']);
        if line.is_empty() {
            break;
        }
        if let Some(value) = line.strip_prefix("Content-Length: ") {
            content_length = value.parse::<usize>().ok();
        }
    }

    let mut body = vec![0; content_length?];
    reader.read_exact(&mut body).await.ok()?;
    serde_json::from_slice(&body).ok()
}

async fn write_lsp_message<W: tokio::io::AsyncWrite + Unpin>(
    writer: &mut W,
    message: &serde_json::Value,
) -> std::io::Result<()> {
    let body = serde_json::to_vec(message)?;
    writer
        .write_all(format!("Content-Length: {}\r\n\r\n", body.len()).as_bytes())
        .await?;
    writer.write_all(&body).await?;
    writer.flush().await
}

fn source_root_uri(params: &InitializeParams) -> Option<Url> {
    params.root_uri.clone().or_else(|| {
        params
            .workspace_folders
            .as_ref()
            .and_then(|folders| folders.first())
            .map(|folder| folder.uri.clone())
    })
}

fn target_rusk_root_uri(source_root_uri: &Url) -> Option<Url> {
    let mut path = source_root_uri.to_file_path().ok()?;
    path.push("target");
    path.push("rusk");
    Url::from_directory_path(path).ok()
}

fn rust_initialize_params(mut params: InitializeParams, rust_root_uri: &Url) -> InitializeParams {
    params.root_uri = Some(rust_root_uri.clone());
    params.workspace_folders = Some(vec![WorkspaceFolder {
        uri: rust_root_uri.clone(),
        name: "rusk-generated".to_string(),
    }]);
    params
}

fn rust_uri_for_rusk_uri(uri: &Url, source_root_uri: &Url, rust_root_uri: &Url) -> Option<Url> {
    let source_path = uri.to_file_path().ok()?;
    let source_root = source_root_uri.to_file_path().ok()?;
    let rust_root = rust_root_uri.to_file_path().ok()?;
    let relative = source_path
        .strip_prefix(&source_root)
        .ok()
        .map(Path::to_path_buf)
        .or_else(|| source_path.file_name().map(PathBuf::from))?;
    let mut path = rust_root.join(relative);
    path.set_extension("rs");
    Url::from_file_path(path).ok()
}

fn copy_generated_cargo_manifests(uri: &Url, source_root_uri: &Url, rust_root_uri: &Url) {
    let Ok(source_path) = uri.to_file_path() else {
        return;
    };
    let Ok(source_root) = source_root_uri.to_file_path() else {
        return;
    };
    let Ok(rust_root) = rust_root_uri.to_file_path() else {
        return;
    };
    let Some(mut current) = source_path.parent() else {
        return;
    };

    while current.starts_with(&source_root) {
        let manifest = current.join("Cargo.toml");
        if manifest.is_file()
            && let Ok(relative) = manifest.strip_prefix(&source_root)
        {
            copy_manifest_if_changed(&manifest, &rust_root.join(relative), current == source_root);
        }
        if current == source_root {
            break;
        }
        let Some(parent) = current.parent() else {
            break;
        };
        current = parent;
    }
}

fn copy_manifest_if_changed(source: &Path, destination: &Path, is_root_manifest: bool) {
    let Ok(mut content) = fs::read_to_string(source) else {
        return;
    };
    if is_root_manifest && !manifest_has_section(&content, "package") {
        content = "[workspace]\n\n[package]\nname = \"rusk-generated\"\nversion = \"0.0.0\"\nedition = \"2024\"\n".to_string();
    } else if is_root_manifest && !manifest_has_section(&content, "workspace") {
        content = format!("[workspace]\n\n{content}");
    }
    if fs::read_to_string(destination).is_ok_and(|existing| existing == content) {
        return;
    }
    if let Some(parent) = destination.parent() {
        let _ = fs::create_dir_all(parent);
    }
    let _ = fs::write(destination, content);
}

fn manifest_has_section(content: &str, section: &str) -> bool {
    content
        .lines()
        .any(|line| line.trim() == format!("[{section}]"))
}

fn write_generated_rust_file(uri: &Url, rust: &str) {
    let Ok(path) = uri.to_file_path() else {
        return;
    };
    let generated = format!("{GENERATED_HEADER}{rust}");
    if let Ok(existing) = fs::read_to_string(&path) {
        if !existing.starts_with(GENERATED_HEADER) || existing == generated {
            return;
        }
    }
    if let Some(parent) = path.parent() {
        let _ = fs::create_dir_all(parent);
    }
    let _ = fs::write(path, generated);
}

fn source_syntax(uri: &Url) -> SourceSyntax {
    match uri
        .to_file_path()
        .ok()
        .and_then(|path| {
            path.extension()
                .and_then(|extension| extension.to_str())
                .map(str::to_string)
        })
        .as_deref()
    {
        Some("rk") => SourceSyntax::Ruk,
        Some("rs") => SourceSyntax::Rust,
        _ => SourceSyntax::Rusk,
    }
}

fn source_position_to_rust_position(
    text: &str,
    syntax: SourceSyntax,
    position: Position,
) -> Option<Position> {
    if syntax != SourceSyntax::Rusk {
        return Some(position);
    }
    let output = transpile(text).ok()?;
    let generated_line = output
        .source_map
        .iter()
        .find(|entry| entry.source_line == position.line as usize + 1)?
        .generated_line
        .saturating_sub(1) as u32;
    let generated_character = output
        .rust
        .lines()
        .nth(generated_line as usize)
        .map(str::len)
        .unwrap_or_default()
        .min(position.character as usize) as u32;
    Some(Position {
        line: generated_line,
        character: generated_character,
    })
}

fn rust_range_to_source_range(text: &str, syntax: SourceSyntax, range: Range) -> Range {
    Range {
        start: rust_position_to_source_position(text, syntax, range.start),
        end: rust_position_to_source_position(text, syntax, range.end),
    }
}

fn rust_position_to_source_position(
    text: &str,
    syntax: SourceSyntax,
    position: Position,
) -> Position {
    if syntax != SourceSyntax::Rusk {
        return position;
    }
    let Ok(output) = transpile(text) else {
        return position;
    };
    let Some(entry) = output
        .source_map
        .iter()
        .find(|entry| entry.generated_line == position.line as usize + 1)
    else {
        return position;
    };
    let source_line = entry.source_line.saturating_sub(1) as u32;
    let source_character = text
        .lines()
        .nth(source_line as usize)
        .map(str::len)
        .unwrap_or_default()
        .min(position.character as usize) as u32;
    Position {
        line: source_line,
        character: source_character,
    }
}

fn rust_definition_to_rusk_definition(
    text: &str,
    syntax: SourceSyntax,
    rusk_uri: &Url,
    rust_uri: &Url,
    definition: GotoDefinitionResponse,
) -> GotoDefinitionResponse {
    match definition {
        GotoDefinitionResponse::Scalar(location) => GotoDefinitionResponse::Scalar(
            rust_location_to_rusk_location(text, syntax, rusk_uri, rust_uri, location),
        ),
        GotoDefinitionResponse::Array(locations) => GotoDefinitionResponse::Array(
            locations
                .into_iter()
                .map(|location| {
                    rust_location_to_rusk_location(text, syntax, rusk_uri, rust_uri, location)
                })
                .collect(),
        ),
        GotoDefinitionResponse::Link(links) => GotoDefinitionResponse::Link(
            links
                .into_iter()
                .map(|link| {
                    rust_location_link_to_rusk_location_link(text, syntax, rusk_uri, rust_uri, link)
                })
                .collect(),
        ),
    }
}

fn rust_location_to_rusk_location(
    text: &str,
    syntax: SourceSyntax,
    rusk_uri: &Url,
    rust_uri: &Url,
    mut location: Location,
) -> Location {
    if rust_uri == &location.uri {
        location.uri = rusk_uri.clone();
        location.range = rust_range_to_source_range(text, syntax, location.range);
    }
    location
}

fn rust_location_link_to_rusk_location_link(
    text: &str,
    syntax: SourceSyntax,
    rusk_uri: &Url,
    rust_uri: &Url,
    mut link: LocationLink,
) -> LocationLink {
    if rust_uri == &link.target_uri {
        link.target_uri = rusk_uri.clone();
        link.target_range = rust_range_to_source_range(text, syntax, link.target_range);
        link.target_selection_range =
            rust_range_to_source_range(text, syntax, link.target_selection_range);
        link.origin_selection_range = link
            .origin_selection_range
            .map(|range| rust_range_to_source_range(text, syntax, range));
    }
    link
}

#[allow(deprecated)]
fn symbol_from_node(node: &SourceMapNode) -> Option<DocumentSymbol> {
    let kind = symbol_kind(&node.kind)?;
    Some(DocumentSymbol {
        name: symbol_name(node),
        detail: Some(node.kind.clone()),
        kind,
        tags: None,
        deprecated: None,
        range: node_range(node),
        selection_range: node_range(node),
        children: Some(node.children.iter().filter_map(symbol_from_node).collect()),
    })
}

fn symbol_kind(kind: &str) -> Option<SymbolKind> {
    match kind {
        "struct" => Some(SymbolKind::STRUCT),
        "enum" => Some(SymbolKind::ENUM),
        "trait" => Some(SymbolKind::INTERFACE),
        "impl" => Some(SymbolKind::OBJECT),
        "module" => Some(SymbolKind::MODULE),
        "function" => Some(SymbolKind::FUNCTION),
        "member" | "field" => Some(SymbolKind::FIELD),
        _ => None,
    }
}

fn symbol_name(node: &SourceMapNode) -> String {
    let text = node.source_text.trim();
    if let Some(name) = item_name(text, "struct")
        .or_else(|| item_name(text, "enum"))
        .or_else(|| item_name(text, "trait"))
        .or_else(|| item_name(text, "mod"))
        .or_else(|| item_name(text, "fn"))
    {
        name
    } else if let Some(rest) = text.strip_prefix("impl") {
        format!("impl{}", rest)
    } else {
        text.split([':', '=', '(', ' '])
            .next()
            .unwrap_or(text)
            .to_string()
    }
}

fn item_name(text: &str, keyword: &str) -> Option<String> {
    let index = text.find(keyword)? + keyword.len();
    let rest = text[index..].trim_start();
    let name = rest
        .split(|character: char| !character.is_ascii_alphanumeric() && character != '_')
        .find(|part| !part.is_empty())?;
    Some(name.to_string())
}

fn node_range(node: &SourceMapNode) -> Range {
    let line = node.source_line.saturating_sub(1) as u32;
    Range {
        start: Position {
            line,
            character: node.source_indent as u32,
        },
        end: Position {
            line,
            character: (node.source_indent + node.source_text.len()) as u32,
        },
    }
}

fn line_range(text: &str, line: usize) -> Range {
    let line_index = line.saturating_sub(1);
    let length = text
        .lines()
        .nth(line_index)
        .map(str::len)
        .unwrap_or_default() as u32;
    Range {
        start: Position {
            line: line_index as u32,
            character: 0,
        },
        end: Position {
            line: line_index as u32,
            character: length,
        },
    }
}

const KEYWORDS: &[&str] = &[
    "as", "async", "await", "break", "const", "continue", "else", "enum", "false", "fn", "for",
    "if", "impl", "in", "let", "loop", "match", "mod", "move", "mut", "pub", "return", "self",
    "Self", "static", "struct", "trait", "true", "type", "unsafe", "use", "where", "while",
];

const BUILTIN_TYPES: &[&str] = &[
    "bool", "char", "f32", "f64", "i8", "i16", "i32", "i64", "i128", "isize", "str", "u8", "u16",
    "u32", "u64", "u128", "usize",
];

#[derive(Debug, Clone, PartialEq, Eq)]
struct Word {
    text: String,
    range: Range,
}

#[derive(Debug, Clone, PartialEq, Eq)]
struct Declaration {
    name: String,
    detail: String,
    kind: CompletionItemKind,
    range: Range,
}

fn hover_at_position(text: &str, position: Position) -> Option<Hover> {
    let word = word_at_position(text, position)?;
    let contents = if BUILTIN_TYPES.contains(&word.text.as_str()) {
        format!("builtin type `{}`", word.text)
    } else if KEYWORDS.contains(&word.text.as_str()) {
        format!("keyword `{}`", word.text)
    } else {
        declaration_at_position(text, &word)?.detail
    };

    Some(Hover {
        contents: HoverContents::Markup(MarkupContent {
            kind: MarkupKind::Markdown,
            value: contents,
        }),
        range: Some(word.range),
    })
}

fn definition_range_at_position(text: &str, position: Position) -> Option<Range> {
    let word = word_at_position(text, position)?;
    declaration_at_position(text, &word).map(|declaration| declaration.range)
}

fn completion_items(text: &str) -> Vec<CompletionItem> {
    let mut seen = BTreeSet::new();
    KEYWORDS
        .iter()
        .map(|keyword| CompletionItem {
            label: (*keyword).to_string(),
            kind: Some(CompletionItemKind::KEYWORD),
            ..CompletionItem::default()
        })
        .chain(BUILTIN_TYPES.iter().map(|builtin| CompletionItem {
            label: (*builtin).to_string(),
            kind: Some(CompletionItemKind::TYPE_PARAMETER),
            detail: Some("builtin type".to_string()),
            ..CompletionItem::default()
        }))
        .chain(
            collect_declarations(text)
                .into_iter()
                .map(|declaration| CompletionItem {
                    label: declaration.name,
                    kind: Some(declaration.kind),
                    detail: Some(declaration.detail),
                    ..CompletionItem::default()
                }),
        )
        .filter(|item| seen.insert(item.label.clone()))
        .collect()
}

fn declaration_at_position(text: &str, word: &Word) -> Option<Declaration> {
    let declarations = collect_declarations(text);
    declarations
        .iter()
        .rev()
        .find(|declaration| {
            declaration.name == word.text && declaration.range.start.line <= word.range.start.line
        })
        .or_else(|| {
            declarations
                .iter()
                .find(|declaration| declaration.name == word.text)
        })
        .cloned()
}

fn collect_declarations(text: &str) -> Vec<Declaration> {
    text.lines()
        .enumerate()
        .flat_map(|(index, raw)| declarations_from_line(index as u32, raw))
        .collect()
}

fn declarations_from_line(line: u32, raw: &str) -> Vec<Declaration> {
    let text = raw.trim_start();
    if text.is_empty() || text.starts_with("//") {
        return Vec::new();
    }

    let indent = raw.len() - text.len();
    let mut declarations = Vec::new();
    if let Some(declaration) =
        item_declaration(line, indent, text, "struct", CompletionItemKind::STRUCT)
            .or_else(|| item_declaration(line, indent, text, "enum", CompletionItemKind::ENUM))
            .or_else(|| {
                item_declaration(line, indent, text, "trait", CompletionItemKind::INTERFACE)
            })
            .or_else(|| item_declaration(line, indent, text, "mod", CompletionItemKind::MODULE))
            .or_else(|| item_declaration(line, indent, text, "fn", CompletionItemKind::FUNCTION))
    {
        if declaration.kind == CompletionItemKind::FUNCTION {
            declarations.extend(parameter_declarations(line, indent, text));
        }
        declarations.push(declaration);
        return declarations;
    }

    if let Some(declaration) = let_declaration(line, indent, text) {
        declarations.push(declaration);
    } else if let Some(declaration) = field_declaration(line, indent, text) {
        declarations.push(declaration);
    }
    declarations
}

fn item_declaration(
    line: u32,
    indent: usize,
    text: &str,
    keyword: &str,
    kind: CompletionItemKind,
) -> Option<Declaration> {
    let keyword_start = keyword_position(text, keyword)?;
    let name_start = text[keyword_start + keyword.len()..]
        .find(is_identifier_start)
        .map(|offset| keyword_start + keyword.len() + offset)?;
    let name_end = identifier_end(text, name_start);
    let name = text[name_start..name_end].to_string();
    Some(Declaration {
        name,
        detail: trim_assignment_tail(text).to_string(),
        kind,
        range: range_for_span(line, indent + name_start, indent + name_end),
    })
}

fn parameter_declarations(line: u32, indent: usize, text: &str) -> Vec<Declaration> {
    let Some(fn_start) = keyword_position(text, "fn") else {
        return Vec::new();
    };
    let Some(open) = text[fn_start..].find('(').map(|offset| fn_start + offset) else {
        return Vec::new();
    };
    let Some(close) = matching_delimiter(text, open, '(', ')') else {
        return Vec::new();
    };

    split_top_level_spans(&text[open + 1..close], ',')
        .into_iter()
        .filter_map(|(start, end)| {
            let parameter = &text[open + 1 + start..open + 1 + end];
            let trimmed_start = parameter.len() - parameter.trim_start().len();
            let trimmed = parameter.trim();
            let colon = trimmed.find(':')?;
            let name = trimmed[..colon].trim();
            if name == "self" || name == "&self" || name == "&mut self" {
                return None;
            }
            let name_start = open + 1 + start + trimmed_start + trimmed[..colon].find(name)?;
            let name_end = name_start + name.len();
            Some(Declaration {
                name: name.to_string(),
                detail: format!("parameter {name}: {}", trimmed[colon + 1..].trim()),
                kind: CompletionItemKind::VARIABLE,
                range: range_for_span(line, indent + name_start, indent + name_end),
            })
        })
        .collect()
}

fn let_declaration(line: u32, indent: usize, text: &str) -> Option<Declaration> {
    let rest = text.strip_prefix("let ")?;
    let rest_offset = text.len() - rest.len();
    let rest = rest.strip_prefix("mut ").unwrap_or(rest);
    let rest_offset = if rest.as_ptr() == text[rest_offset..].as_ptr() {
        rest_offset
    } else {
        rest_offset + "mut ".len()
    };
    let name_start = rest.find(is_identifier_start)?;
    let name_end = identifier_end(rest, name_start);
    let name = &rest[name_start..name_end];
    let after_name = &rest[name_end..];
    let detail = after_name
        .trim_start()
        .strip_prefix(':')
        .and_then(|after_colon| after_colon.split('=').next())
        .map(str::trim)
        .filter(|annotation| !annotation.is_empty())
        .map(|annotation| format!("let {name}: {annotation}"))
        .unwrap_or_else(|| format!("let {name}"));

    Some(Declaration {
        name: name.to_string(),
        detail,
        kind: CompletionItemKind::VARIABLE,
        range: range_for_span(
            line,
            indent + rest_offset + name_start,
            indent + rest_offset + name_end,
        ),
    })
}

fn field_declaration(line: u32, indent: usize, text: &str) -> Option<Declaration> {
    let (text, offset) = text
        .strip_prefix("pub ")
        .map(|text| (text, "pub ".len()))
        .unwrap_or((text, 0));
    let colon = text.find(':')?;
    if text[..colon].contains([' ', '(', ')', '=']) {
        return None;
    }
    let name = text[..colon].trim();
    if name.is_empty() || !name.chars().next().is_some_and(is_identifier_start) {
        return None;
    }
    Some(Declaration {
        name: name.to_string(),
        detail: format!("field {name}: {}", text[colon + 1..].trim()),
        kind: CompletionItemKind::FIELD,
        range: range_for_span(line, indent + offset, indent + offset + name.len()),
    })
}

fn word_at_position(text: &str, position: Position) -> Option<Word> {
    let raw = text.lines().nth(position.line as usize)?;
    let character = (position.character as usize).min(raw.len());
    let bytes = raw.as_bytes();
    let mut start = character;
    if start == raw.len() && start > 0 {
        start -= 1;
    }
    if start < raw.len()
        && !is_identifier_byte(bytes[start])
        && start > 0
        && is_identifier_byte(bytes[start - 1])
    {
        start -= 1;
    }
    if start >= raw.len() || !is_identifier_byte(bytes[start]) {
        return None;
    }
    while start > 0 && is_identifier_byte(bytes[start - 1]) {
        start -= 1;
    }
    let mut end = start;
    while end < raw.len() && is_identifier_byte(bytes[end]) {
        end += 1;
    }

    Some(Word {
        text: raw[start..end].to_string(),
        range: range_for_span(position.line, start, end),
    })
}

fn keyword_position(text: &str, keyword: &str) -> Option<usize> {
    text.match_indices(keyword)
        .find(|(index, _)| {
            let before = index
                .checked_sub(1)
                .and_then(|before| text.as_bytes().get(before))
                .is_none_or(|byte| !is_identifier_byte(*byte));
            let after = text
                .as_bytes()
                .get(index + keyword.len())
                .is_none_or(|byte| !is_identifier_byte(*byte));
            before && after
        })
        .map(|(index, _)| index)
}

fn identifier_end(text: &str, start: usize) -> usize {
    let bytes = text.as_bytes();
    let mut end = start;
    while end < text.len() && is_identifier_byte(bytes[end]) {
        end += 1;
    }
    end
}

fn is_identifier_start(character: char) -> bool {
    character == '_' || character.is_ascii_alphabetic()
}

fn is_identifier_byte(byte: u8) -> bool {
    byte == b'_' || byte.is_ascii_alphanumeric()
}

fn trim_assignment_tail(text: &str) -> &str {
    text.split(" =")
        .next()
        .unwrap_or(text)
        .trim_end_matches('=')
        .trim()
}

fn matching_delimiter(text: &str, open: usize, open_char: char, close_char: char) -> Option<usize> {
    let mut depth = 0usize;
    for (index, character) in text[open..].char_indices() {
        if character == open_char {
            depth += 1;
        } else if character == close_char {
            depth = depth.saturating_sub(1);
            if depth == 0 {
                return Some(open + index);
            }
        }
    }
    None
}

fn split_top_level_spans(text: &str, delimiter: char) -> Vec<(usize, usize)> {
    let mut spans = Vec::new();
    let mut start = 0usize;
    let mut parens = 0usize;
    let mut brackets = 0usize;
    let mut braces = 0usize;
    for (index, character) in text.char_indices() {
        match character {
            '(' => parens += 1,
            ')' => parens = parens.saturating_sub(1),
            '[' => brackets += 1,
            ']' => brackets = brackets.saturating_sub(1),
            '{' => braces += 1,
            '}' => braces = braces.saturating_sub(1),
            character if character == delimiter && parens == 0 && brackets == 0 && braces == 0 => {
                spans.push((start, index));
                start = index + character.len_utf8();
            }
            _ => {}
        }
    }
    spans.push((start, text.len()));
    spans
}

fn range_for_span(line: u32, start: usize, end: usize) -> Range {
    Range {
        start: Position {
            line,
            character: start as u32,
        },
        end: Position {
            line,
            character: end as u32,
        },
    }
}

